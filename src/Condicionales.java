public class Condicionales {
    public static void main(String[] args) throws Exception{
        for(int i=1;i<=10;i++){
            if (i<6) {
                System.out.println(i+" Te falto esforzarte mas");
            }
            if (i==6 || i==7) {
                System.out.println(i+" de panzaso xd");
            }

            if(i==8 || i==9){
                System.out.println(i+ " Muy bien, puedes mejorar mas");
            }
            if (i==10) {
                System.out.println(i+" Bien hecho");
            }
        }
    }
}
